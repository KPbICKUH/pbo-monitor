# Получаем PID родителя
$parentPID = (Get-WmiObject -Class Win32_Process -Filter "ProcessId = $PID").ParentProcessId

# Функция для активации окна
function Show-Window {
    $sig = '[DllImport("user32.dll")] public static extern bool ShowWindowAsync(IntPtr hWnd, int nCmdShow);'
    $type = Add-Type -MemberDefinition $sig -Name WindowUtils -Namespace Win32Functions -PassThru
    $hwnd = (Get-Process -Id $PID).MainWindowHandle
    if ($hwnd -ne 0) {
        $type::ShowWindowAsync($hwnd, 9) | Out-Null
    }
}

# Активируем окно каждые 2 секунды
$timer = [System.Diagnostics.Stopwatch]::StartNew()

while ($true) {
    if (-not (Get-Process -Id $parentPID -ErrorAction SilentlyContinue)) {
        exit
    }
    
    if ($timer.Elapsed.TotalSeconds -gt 2) {
        Show-Window
        $timer.Restart()
    }
    
    foreach ($f in $frames) {
        Clear-Host
        
        # Выбираем цвет в зависимости от содержимого кадра
        if ($f -match "БАХ|ОГОНЬ|КРАХ|УДАР|НОКАУТ|ВЗРЫВ") {
            Write-Host $f -ForegroundColor Red
        } elseif ($f -match "POLICE|Полиция") {
            Write-Host $f -ForegroundColor Blue
        } elseif ($f -match "БЭТМЕН|ГЕРОЙ|уходит") {
            Write-Host $f -ForegroundColor Yellow
        } elseif ($f -match "ДЖОКЕР|HA-HA|Джокер") {
            Write-Host $f -ForegroundColor Magenta
        } elseif ($f -match "ПОГОНЯ|догоняет|ПАДАЕТ") {
            Write-Host $f -ForegroundColor Cyan
        } elseif ($f -match "ПРОДОЛЖЕНИЕ|CONTINUED|спасен") {
            Write-Host $f -ForegroundColor Green
        } elseif ($f -match "Привет|Скучал|философии|Сюрприз|Пока|Вернись") {
            Write-Host $f -ForegroundColor White
        } else {
            Write-Host $f -ForegroundColor Gray
        }
        
        # Разная скорость для разных сцен
        if ($f -match "ВЗРЫВ|НОКАУТ|ВСПЛЕСК") {
            Start-Sleep -Milliseconds 900
        } elseif ($f -match "диалог|Привет|Скучал|философии|бургер|пугают|Сюрприз") {
            Start-Sleep -Milliseconds 1200
        } elseif ($f -match "ГРАНАТА|ПАДАЕТ|тонет") {
            Start-Sleep -Milliseconds 700
        } elseif ($f -match "БАХ|УДАР|КРАХ") {
            Start-Sleep -Milliseconds 400
        } elseif ($f -match "ПОГОНЯ|догоняет") {
            Start-Sleep -Milliseconds 200
        } elseif ($f -match "ПРОДОЛЖЕНИЕ|CONTINUED") {
            Start-Sleep -Milliseconds 2000
        } else {
            Start-Sleep -Milliseconds 250
        }
    }
    
    # Пауза перед повтором
    Start-Sleep -Seconds 3
}